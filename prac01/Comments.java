class Comments
{
    public static void main(String[] args)
    {
        System.out.println("Program comments are nonexecuting statements" +
                "you add to a file for the purpose of documentation.");

        // line comments
        // Program comments are nonexecuting statements
        // you add to a file for the purpose of documentation

        // block comments
        /* Program comments are nonexecuting statements
        you add to a file for the purpose of documentation */

        // javadoc comments
        /** Program comments are nonexecuting statements
         * you add to a file for the purpose of documentation */

    }
}